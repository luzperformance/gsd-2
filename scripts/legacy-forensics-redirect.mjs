#!/usr/bin/env node
// Project/App: GSD-2
// File Purpose: Close legacy /gsd forensics issues with a redirect to the active Open GSD repo.

import { readFileSync } from "node:fs";

export const FORENSICS_MARKER = "Auto-generated by `/gsd forensics`";
export const REDIRECT_MARKER = "<!-- gsd-legacy-forensics-redirect -->";
export const OPEN_GSD_ISSUES_URL = "https://github.com/open-gsd/gsd-pi/issues";

export function isLegacyForensicsIssue(issue) {
  if (!issue || issue.pull_request) return false;
  return String(issue.body || "").includes(FORENSICS_MARKER);
}

export function buildRedirectComment() {
  return [
    REDIRECT_MARKER,
    "Thanks for running `/gsd forensics`.",
    "",
    "This legacy repository is no longer the active issue tracker. Please open or continue this report in the new Open GSD repo:",
    "",
    OPEN_GSD_ISSUES_URL,
    "",
    "Closing this issue here so forensics reports do not get triaged in the retired repository.",
  ].join("\n");
}

export function hasRedirectComment(comments) {
  return comments.some((comment) => String(comment.body || "").includes(REDIRECT_MARKER));
}

function env(name) {
  const value = process.env[name];
  if (!value) throw new Error(`${name} is required`);
  return value;
}

async function githubJson(path, options = {}) {
  const token = env("GITHUB_TOKEN");
  const response = await fetch(`https://api.github.com${path}`, {
    ...options,
    headers: {
      accept: "application/vnd.github+json",
      authorization: `Bearer ${token}`,
      "content-type": "application/json",
      "x-github-api-version": "2022-11-28",
      ...(options.headers || {}),
    },
  });

  if (!response.ok) {
    const body = await response.text();
    throw new Error(`GitHub API request failed: ${response.status} ${body}`);
  }

  if (response.status === 204) return null;
  return response.json();
}

export async function redirectLegacyForensicsIssue(githubJsonFn, owner, repo, issue) {
  if (!isLegacyForensicsIssue(issue)) {
    return "skipped";
  }

  const issueNumber = issue.number;
  const comments = await githubJsonFn(`/repos/${owner}/${repo}/issues/${issueNumber}/comments?per_page=100`);

  if (!hasRedirectComment(comments)) {
    await githubJsonFn(`/repos/${owner}/${repo}/issues/${issueNumber}/comments`, {
      method: "POST",
      body: JSON.stringify({ body: buildRedirectComment() }),
    });
  }

  if (issue.state !== "closed") {
    await githubJsonFn(`/repos/${owner}/${repo}/issues/${issueNumber}`, {
      method: "PATCH",
      body: JSON.stringify({ state: "closed", state_reason: "not_planned" }),
    });
  }

  return "redirected";
}

async function run() {
  const event = JSON.parse(readFileSync(env("GITHUB_EVENT_PATH"), "utf8"));
  const [owner, repo] = env("GITHUB_REPOSITORY").split("/");
  const result = await redirectLegacyForensicsIssue(githubJson, owner, repo, event.issue);
  console.log(`Legacy forensics redirect ${result}.`);
}

if (import.meta.url === `file://${process.argv[1]}`) {
  run().catch((error) => {
    console.error(error.message);
    process.exit(1);
  });
}
